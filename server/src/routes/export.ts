// ─── Export from URL (scrape + generate + package ZIP) ──────────────────────
router.post("/export/from-url", async (req, res) => {
  const { url, marketplace = "eBay UK", storeName = "My eBay Store", bannerTagline = "Quality Products, Fast Shipping" } = req.body as {
    url: string;
    marketplace?: string;
    storeName?: string;
    bannerTagline?: string;
  };

  if (!url) { res.status(400).json({ error: "URL required" }); return; }

  try {
    // Step 1: Scrape product data
    const scraped = await scrapeUrl(url);
    const rawText = [scraped.title, scraped.description, ...Object.values(scraped.specs ?? {})].join(" ");
    const extractedKeywords = extractKeywordsFromText(rawText);

    const product: ProductData = {
      title: scraped.title,
      description: scraped.description,
      brand: scraped.brand,
      price: scraped.price,
      specs: scraped.specs,
      keywords: extractedKeywords.slice(0, 10),
      sourceUrl: url,
    };

    // Step 2: Generate listing
    const prompt = buildListingPrompt(product, { marketplace });
    const listing = await callAIJSON<ListingExportData>(prompt, "listing", SYSTEM_PROMPTS.listing);

    // Step 3: Process images (download + upscale to 4x)
    const zip = new JSZip();
    const imagesFolder = zip.folder("images")!;
    let processedImageCount = 0;

    const imageUrls = (scraped.images ?? []).slice(0, 12).filter(Boolean);
    await Promise.allSettled(
      imageUrls.map(async (imgUrl, i) => {
        try {
          const imgRes = await fetch(imgUrl, {
            headers: { "User-Agent": "Mozilla/5.0 (compatible; Pixlance/1.0)" },
            signal: AbortSignal.timeout(10000),
          });
          if (!imgRes.ok) return;
          const imgBuffer = Buffer.from(await imgRes.arrayBuffer());

          // Upscale 4x with sharp
          const meta = await sharp(imgBuffer).metadata();
          const targetW = Math.min((meta.width ?? 400) * 4, 3200);
          const targetH = Math.min((meta.height ?? 400) * 4, 3200);

          const upscaled = await sharp(imgBuffer)
            .resize(targetW, targetH, { kernel: sharp.kernel.lanczos3, fit: "fill" })
            .sharpen(1.2, 1, 0.5)
            .jpeg({ quality: 95, chromaSubsampling: "4:4:4" })
            .toBuffer();

          imagesFolder.file(`image_${i + 1}_${targetW}x${targetH}.jpg`, upscaled);
          processedImageCount++;
        } catch { /* skip failed images */ }
      })
    );

    // Step 4: Generate store banner (1200×200)
    const bannerSvg = `<svg width="1200" height="200" xmlns="http://www.w3.org/2000/svg">
      <defs><linearGradient id="bg" x1="0%" y1="0%" x2="100%" y2="100%">
        <stop offset="0%" style="stop-color:#0f172a"/><stop offset="100%" style="stop-color:#1e3a5f33"/>
      </linearGradient></defs>
      <rect width="1200" height="200" fill="url(#bg)"/>
      <rect x="0" y="196" width="1200" height="4" fill="#3b82f6"/>
      <text x="600" y="84" font-family="Arial,sans-serif" font-size="56" font-weight="bold" fill="#fff" text-anchor="middle" dominant-baseline="middle">${storeName.replace(/[<>&]/g, "")}</text>
      <text x="600" y="144" font-family="Arial,sans-serif" font-size="28" fill="#3b82f6" text-anchor="middle" dominant-baseline="middle">${bannerTagline.replace(/[<>&]/g, "")}</text>
    </svg>`;
    const bannerBuffer = await sharp(Buffer.from(bannerSvg)).resize(1200, 200).jpeg({ quality: 95 }).toBuffer();
    zip.file("banner.jpg", bannerBuffer);

    // Step 5: Build HTML listing
    const html = buildEbayHTML(listing, storeName);
    zip.file("listing.html", html);

    // Step 6: JSON export
    const seoReport = generateSEOReport({
      title: listing.title ?? "",
      description: listing.description ?? "",
      itemSpecifics: listing.itemSpecifics ?? {},
      price: listing.price,
      category: listing.category,
    });
    zip.file("listing.json", JSON.stringify({ listing, seoReport, scraped: { title: scraped.title, sourceDomain: scraped.sourceDomain, scrapedAt: scraped.scrapedAt } }, null, 2));

    // Step 7: Keywords CSV
    const kwCsv = ["Keyword,Type\n",
      ...(listing.keywords ?? []).map(k => `"${k}",primary\n`),
      ...(listing.tags ?? []).map(t => `"${t}",tag\n`),
    ].join("");
    zip.file("keywords.csv", kwCsv);

    // Step 8: README
    zip.file("README.txt", buildReadme(listing, processedImageCount));

    // Generate ZIP
    const zipBuffer = await zip.generateAsync({ type: "nodebuffer", compression: "DEFLATE", compressionOptions: { level: 6 } });

    const safeName = (listing.title ?? "listing").slice(0, 40).replace(/[^a-z0-9]/gi, "_").toLowerCase();
    res.set({
      "Content-Type": "application/zip",
      "Content-Disposition": `attachment; filename="pixlance_${safeName}.zip"`,
      "Content-Length": zipBuffer.length.toString(),
    });
    res.send(zipBuffer);

    // Track usage async
    void trackUsage("listing_exported_url", undefined, req.ip ?? undefined, { url: url.slice(0, 100), imageCount: processedImageCount });
  } catch (err) {
    res.status(500).json({ error: String(err) });
  }
});

