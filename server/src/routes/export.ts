/**
 * Full Listing Export — generates a downloadable ZIP containing:
 * - Upscaled product images (4x, eBay optimized)
 * - Store banner (1200×200)
 * - eBay listing as HTML (ready to paste)
 * - Listing data as JSON
 * - Keywords CSV
 * - README.txt with instructions
 */

import { Router } from "express";
import multer from "multer";
import JSZip from "jszip";
import sharp from "sharp";
import { callAIJSON } from "../ai/models.js";
import { buildListingPrompt, SYSTEM_PROMPTS, type ProductData } from "../ai/prompts.js";
import { scrapeUrl, extractKeywordsFromText } from "../scraper/index.js";
import { generateSEOReport } from "../ebay/seo.js";
import { trackUsage } from "../db/index.js";

const router = Router();

const upload = multer({
  storage: multer.memoryStorage(),
  limits: { fileSize: 50 * 1024 * 1024, files: 20 },
});

interface ListingExportData {
  title: string;
  subtitle?: string;
  category: string;
  condition: string;
  price: number;
  description: string;
  itemSpecifics: Record<string, string>;
  keywords: string[];
  tags?: string[];
  shippingRecommendation?: string;
  seoScore?: number;
  cassiniTips?: string[];
}

